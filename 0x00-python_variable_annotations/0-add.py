#!/usr/bin/env python3
"""
This script defines a function to add two float numbers.

Author: Your Name
"""

def add(a: float, b: float) -> float:
    """
    Add two float numbers.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b

