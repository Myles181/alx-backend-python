#!/usr/bin/env python3
"""
This script demonstrates a function to calculate the floor of a floating-point number.

It imports the math module to use the math.floor() function for calculating the floor.

Author: Your Name
"""

import math

def floor(n: float) -> int:
    """
    Calculate the floor of a floating-point number.

    Args:
        n (float): The input floating-point number.

    Returns:
        int: The floor value of the input number.
    """
    return math.floor(n)